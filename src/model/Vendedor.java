package cinema;

public class Vendedor {
	private String nome, senha;
	/*************************************************/
	public Vendedor(){
		
	}
	
	
	
	
	/*************************************************/
	/*              Getters and Setters              */
	/*************************************************/
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getSenha() {
		return senha;
	}
	public void setSenha(String senha) {
		this.senha = senha;
	}
}
